package prime.admin.notiftemplate;

public class NotifTemplateBean {
	private Integer notifTemplateId;
	private String notifTemplateName;
	private String notifTemplateValue;
	private String notifTemplateDescription;
	
	public Integer getNotifTemplateId() {
		return notifTemplateId;
	}
	public void setNotifTemplateId(Integer notifTemplateId) {
		this.notifTemplateId = notifTemplateId;
	}
	public String getNotifTemplateName() {
		return notifTemplateName;
	}
	public void setNotifTemplateName(String notifTemplateName) {
		this.notifTemplateName = notifTemplateName;
	}
	public String getNotifTemplateValue() {
		return notifTemplateValue;
	}
	public void setNotifTemplateValue(String notifTemplateValue) {
		this.notifTemplateValue = notifTemplateValue;
	}
	public String getNotifTemplateDescription() {
		return notifTemplateDescription;
	}
	public void setNotifTemplateDescription(String notifTemplateDescription) {
		this.notifTemplateDescription = notifTemplateDescription;
	}
	
	
	
}
